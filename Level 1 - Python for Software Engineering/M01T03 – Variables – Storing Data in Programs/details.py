# Asking user for their details and printing this in a single line

name = input("What is your name?")
age = input("What is your age?")
house_number = input("What is your house number?")
street_name = input("What is your street name?")
print(f"This is {name}. She is {age} years old and lives at house number {house_number} on {street_name}!")
